#*and (y)
resutado1 = True & True #*retorna true (solo retorna true si ambas condiciones son true) 
resutado2 = True & False #*retorna false
resutado3 = False & True #*retorna false
resutado4 = False & False #*retorna false

#*or (o) 
resutado5 = True | True #*Retorna true
resutado6 = True | False #*Retorna true
resutado7 = False | True #*Retorna true(solo se necesita un true para que retore true)
resutado8 = False | False #*Retorna false (Solo retorna false si ambas son false)

#*not (no)
resutado9 = not True #*retorna fale
resutado10 = not False #*retorna true

